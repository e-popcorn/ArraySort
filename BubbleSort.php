    public function sort() {
        $j = 0;
        $tmp = null;
        while ($this->swapped) {
            $this->swapped = false;
            $j++;
            for ($i = 0;$i < count($this->Array) - $j;$i++) {
                if ($this->Array[$i] > $this->Array[$i + 1]) {
                    $tmp = $this->Array[$i];
                    $this->Array[$i] = $this->Array[$i + 1];
                    $this->Array[$i + 1] = $tmp;
                    $this->swapped = true;
                }        
            }
        }
        $this->sorted=true;
    }

